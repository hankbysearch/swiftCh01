    像是：firstName、somePerson。
 Swift 中通常命名常數、變數、函式、屬性、方法及下標時
    會使用小駝峰式命名法。
 
 大駝峰式命名法(upper camel case)
 
 每個單字的首字母都使用大寫字母，像是LastName、SomeClass。
 Swift 中通常命名列舉、結構、類別、擴展、協定及其他自定義型別時
